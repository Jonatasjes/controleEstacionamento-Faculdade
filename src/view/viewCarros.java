    public void readJTable () {
    
        DefaultTableModel modelo = (DefaultTableModel) tbCarros.getModel();
        
        modelo.setNumRows(0);
        
        CarroDAO cdao = new CarroDAO();
        
        cdao.read().forEach((c) -> {
            modelo.addRow(new Object[]{
                c.getMarca(),
                c.getPlaca(),
                c.getCor(),
                c.getHoraEntrada(),
                c.getHoraSaida()
            });
        });
        
    }

