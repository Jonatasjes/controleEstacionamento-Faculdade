    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        
        Carro c = new Carro();
        CarroDAO dao = new CarroDAO();
        c.setMarca(txtMarca.getText());
        c.setPlaca(txtPlaca.getText());
        c.setCor(txtCor.getText());
        c.setHoraEntrada(Integer.parseInt(txtHoraEntrada.getText()));
        c.setHoraSaida(Integer.parseInt(txtHoraSaida.getText()));
        
        dao.create(c);
        
        txtMarca.setText("");
        txtPlaca.setText("");
        txtCor.setText("");
        txtHoraEntrada.setText("");
        txtHoraSaida.setText("");
                
        readJTable();

    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        
        if ( tbCarros.getSelectedRow() != -1 ) {
        
            Carro c = new Carro();
            CarroDAO dao = new CarroDAO();
            
            c.setPlaca((String) tbCarros.getValueAt(tbCarros.getSelectedRow(), 1));

            dao.delete(c);
            
            txtMarca.setText("");
            txtPlaca.setText("");
            txtCor.setText("");
            txtHoraEntrada.setText("");
            txtHoraSaida.setText("");

            readJTable();
        
        }
        
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // TODO add your handling code here:
        
        if ( tbCarros.getSelectedRow() != -1 ) {
        
            Carro c = new Carro();
            CarroDAO dao = new CarroDAO();
            c.setMarca(txtMarca.getText());
            c.setPlaca(txtPlaca.getText());
            c.setCor(txtCor.getText());
            c.setHoraEntrada(Integer.parseInt(txtHoraEntrada.getText()));
            c.setHoraSaida(Integer.parseInt(txtHoraSaida.getText()));

            dao.update(c, tbCarros.getValueAt(tbCarros.getSelectedRow(), 1).toString());

            txtMarca.setText("");
            txtPlaca.setText("");
            txtCor.setText("");
            txtHoraEntrada.setText("");
            txtHoraSaida.setText("");

            readJTable();

        } 
        
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void tbCarrosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbCarrosMouseClicked
        // TODO add your handling code here:

        if ( tbCarros.getSelectedRow() != -1 ) {
            
            txtMarca.setText(tbCarros.getValueAt(tbCarros.getSelectedRow(), 0).toString());
            txtPlaca.setText(tbCarros.getValueAt(tbCarros.getSelectedRow(), 1).toString());
            txtCor.setText(tbCarros.getValueAt(tbCarros.getSelectedRow(), 2).toString());
            txtHoraEntrada.setText(tbCarros.getValueAt(tbCarros.getSelectedRow(), 3).toString());
            txtHoraSaida.setText(tbCarros.getValueAt(tbCarros.getSelectedRow(), 4).toString());
            

        } 
    }//GEN-LAST:event_tbCarrosMouseClicked

